#[cfg(test)]
mod tests {
    use super::{LoginVerifyRequest, RegisterRequest};
    use chrono::NaiveDate;
    use validator::Validate;

    #[test]
    fn register_accepts_optional_legacy_password() {
        let payload = RegisterRequest {
            username: "user".to_string(),
            display_name: "User".to_string(),
            email: "user@example.com".to_string(),
            srp_salt: "AQID".to_string(),
            srp_verifier: "AQID".to_string(),
            password: Some("1234567890".to_string()),
            date_of_birth: NaiveDate::from_ymd_opt(2000, 1, 1).unwrap(),
            locale: "en".to_string(),
        };

        assert!(payload.validate().is_ok());
    }

    #[test]
    fn register_rejects_invalid_srp_base64() {
        let payload = RegisterRequest {
            username: "user".to_string(),
            display_name: "User".to_string(),
            email: "user@example.com".to_string(),
            srp_salt: "%%%".to_string(),
            srp_verifier: "AQID".to_string(),
            password: None,
            date_of_birth: NaiveDate::from_ymd_opt(2000, 1, 1).unwrap(),
            locale: "en".to_string(),
        };

        assert!(payload.validate().is_err());
    }

    #[test]
    fn login_verify_rejects_non_base64_payloads() {
        let payload = LoginVerifyRequest {
            email: "user@example.com".to_string(),
            client_public_key_a: "%%%".to_string(),
            client_proof_m1: "AQID".to_string(),
            totp_code: None,
            backup_code: None,
        };

        assert!(payload.validate().is_err());
    }
}
