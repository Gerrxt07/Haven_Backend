use base64::{engine::general_purpose::STANDARD, Engine as _};
use chrono::{DateTime, NaiveDate, Utc};
use serde::{Deserialize, Serialize};
use validator::{Validate, ValidationError};

const SRP_SALT_MAX_LEN: u64 = 256;
const SRP_VERIFIER_MAX_LEN: u64 = 2048;
const SRP_PUBLIC_KEY_MAX_LEN: u64 = 2048;
const SRP_PROOF_MAX_LEN: u64 = 2048;

fn validate_base64(value: &str) -> Result<(), ValidationError> {
    STANDARD
        .decode(value)
        .map(|_| ())
        .map_err(|_| ValidationError::new("base64"))
}

#[derive(Deserialize, Validate)]
pub struct RegisterRequest {
    #[validate(length(min = 3, max = 32))]
    pub username: String,
    #[validate(length(min = 1, max = 64))]
    pub display_name: String,
    #[validate(email)]
    pub email: String,
    /// SRP salt (base64 encoded) - generated by client
    #[validate(length(min = 1, max = SRP_SALT_MAX_LEN), custom(function = "validate_base64"))]
    pub srp_salt: String,
    /// SRP verifier (base64 encoded) - computed by client
    #[validate(length(min = 1, max = SRP_VERIFIER_MAX_LEN), custom(function = "validate_base64"))]
    pub srp_verifier: String,
    #[serde(default)]
    pub password: Option<String>,
    pub date_of_birth: NaiveDate,
    #[validate(length(min = 2, max = 10))]
    pub locale: String,
}

#[derive(Deserialize, Validate)]
pub struct LoginChallengeRequest {
    #[validate(email)]
    pub email: String,
}

#[derive(Serialize)]
pub struct LoginChallengeResponse {
    pub challenge_id: String,
    pub srp_salt: String,
    pub server_public_key_b: String,
}

#[derive(Deserialize, Validate)]
pub struct LoginVerifyRequest {
    #[validate(email)]
    pub email: String,
    /// Client public key A (base64 encoded)
    #[validate(length(min = 1, max = SRP_PUBLIC_KEY_MAX_LEN), custom(function = "validate_base64"))]
    pub client_public_key_a: String,
    /// Client proof M1 (base64 encoded)
    #[validate(length(min = 1, max = SRP_PROOF_MAX_LEN), custom(function = "validate_base64"))]
    pub client_proof_m1: String,
    pub totp_code: Option<String>,
    pub backup_code: Option<String>,
}

#[derive(Serialize)]
pub struct LoginVerifyResponse {
    pub server_proof_m2: String,
    pub access_token: String,
    pub refresh_token: String,
    pub token_type: &'static str,
    pub expires_in_seconds: i64,
}

